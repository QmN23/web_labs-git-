<?php


// 1. Среднее арифметическое элементов массива (без цикла)
$numbers = [4, 8, 15, 16, 23, 42];
$average = array_sum($numbers) / count($numbers);
echo "Среднее арифметическое: $average\n";

// 2. Сумма чисел от 1 до 100 (без цикла)
$sumTo100 = array_sum(range(1, 100)); // range создаёт массив от 1 до 100
echo "Сумма от 1 до 100: $sumTo100\n";

// 3. Массив квадратных корней из чисел (без цикла)
$nums = [4, 9, 16, 25];
$sqrts = array_map('sqrt', $nums); // применяем sqrt к каждому элементу
print_r($sqrts); // выводим массив квадратных корней

// 4. Массив с ключами — буквами, значениями — числами от 1 до 26 (без цикла)
$letters = range('a', 'z'); // массив букв от 'a' до 'z'
$values = range(1, 26); // массив чисел от 1 до 26
$alphabetMap = array_combine($letters, $values); // объединяем массивы: буквы => числа
print_r($alphabetMap);

// 5. Сумма пар чисел из строки '1234567890' => 12+34+56+78+90 (без цикла)
$str = '1234567890';
// Разбиваем строку на массив каждых 2 символов
$pairs = str_split($str, 2); // ['12', '34', '56', '78', '90']
// Преобразуем строки в числа и суммируем
$sumPairs = array_sum(array_map('intval', $pairs)); // array_map, intval для преобразования строк в числа
echo "Сумма пар: $sumPairs\n";

