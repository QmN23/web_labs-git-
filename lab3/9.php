<?php

// 1. Массив: 'x', 'xx', 'xxx', ...
$symbols = []; // создаем пустой массив
for ($i = 1; $i <= 5; $i++) { // цикл от 1 до 5
    $symbols[] = str_repeat('x', $i); // добавляем строку, где 'x' повторяется $i раз
}
print_r($symbols); // выводим полученный массив

// 2. Функция arrayFill
function arrayFill($value, $count): array
{ // функция принимает значение и количество повторений
    $result = []; // создаем пустой массив
    for ($i = 0; $i < $count; $i++) { // цикл от 0 до $count
        $result[] = $value; // добавляем значение в массив
    }
    return $result; // возвращаем готовый массив
}
print_r(arrayFill('x', 5)); // вызываем функцию с 'x' и 5 — выведет массив из 5 'x'

// 3. Сумма элементов двумерного массива
$matrix = [[1, 2, 3], [4, 5], [6]]; // объявляем двумерный массив
$sum = 0; // переменная для хранения суммы
foreach ($matrix as $subarray) { // перебираем подмассивы
    foreach ($subarray as $num) { // перебираем элементы в подмассиве
        $sum += $num; // добавляем к общей сумме
    }
}
echo "Сумма всех элементов: $sum\n"; // выводим результат

// 4. Двумерный массив [[1,2,3],[4,5,6],[7,8,9]]
$multiArray = []; // создаем пустой массив
$value = 1; // стартовое значение для заполнения
for ($i = 0; $i < 3; $i++) { //  цикл на три строки
    $row = []; // создаем строку
    for ($j = 0; $j < 3; $j++) { // внутренний цикл — 3 столбца
        $row[] = $value++; // добавляем значение и увеличиваем его
    }
    $multiArray[] = $row; // добавляем строку в массив
}
print_r($multiArray); // выводим двумерный массив

// 5. Умножение элементов массива
$arr = [2, 5, 3, 9]; // массив из 4 элементов
$result = ($arr[0] * $arr[1]) + ($arr[2] * $arr[3]); // перемножаем пары и складываем
echo "Результат умножений и сложения: $result\n"; // выводим результат

// 6. Массив $user с ФИО
$user = [
    'name' => 'Артем', // имя
    'surname' => 'Слободчиков', // фамилия
    'patronymic' => 'Дмитриевич' // отчество
];
echo $user['surname'] . ' ' . $user['name'] . ' ' . $user['patronymic'] . "\n"; // выводим ФИО через пробел

// 7. Массив $date с текущей датой
$date = [
    'year' => 2025,
    'month' => 04,
    'day' => 18
];
echo $date['year'] . '-' . $date['month'] . '-' . $date['day'] . "\n"; // выводим дату в формате ГГГГ-ММ-ДД

// 8. Количество элементов массива
$arr = ['a', 'b', 'c', 'd', 'e']; // простой массив
echo "Количество элементов: " . count($arr) . "\n"; // выводим количество элементов с помощью count()

// 9. Последний элемент
echo "Последний элемент: " . $arr[count($arr) - 1] . "\n"; // последний элемент — по индексу count-1

// 10. Предпоследний элемент
echo "Предпоследний элемент: " . $arr[count($arr) - 2] . "\n"; // предпоследний элемент — по индексу count-2

