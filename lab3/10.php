<?php
// 1. Функция: сумма двух чисел больше 10?
function isSumMoreThanTen($a, $b): bool
{
    return ($a + $b) > 10; // если сумма больше 10, возвращаем true, иначе false
}
echo isSumMoreThanTen(5, 6) ? 'true' : 'false'; // пример вызова
echo "\n";

// 2. Функция: равны ли два числа?
function isEqual($a, $b): bool
{
    return $a === $b; // строгое сравнение: возвращает true, если значения и типы совпадают
}
echo isEqual(3, 3) ? 'true' : 'false'; // пример вызова
echo "\n";

// 3. Сокращённая форма if
$test = 0;
if ($test == 0) echo 'верно'; // сокращенная форма (без фигурных скобок)
echo "\n";

// 4. Работа с переменной $age и анализ суммы цифр
$age = 47; // произвольный возраст

if ($age < 10 || $age > 99) { // если меньше 10 или больше 99 — выводим сообщение
    echo "Число $age вне диапазона (10–99)\n";
} else {
    // если число в диапазоне — находим сумму его цифр
    $digits = str_split((string)$age); // разбиваем число на массив цифр
    $sum = array_sum($digits); // считаем сумму цифр
    if ($sum <= 9) {
        echo "Сумма цифр $sum — однозначна\n";
    } else {
        echo "Сумма цифр $sum — двузначна\n";
    }
}

// 5. Проверка: массив содержит ровно 3 элемента
$arr = [3, 5, 7]; // массив с числами
if (count($arr) === 3) { // если элементов ровно 3
    $sum = array_sum($arr); // считаем их сумму
    echo "Сумма элементов массива: $sum\n"; // выводим результат
}

